int ft_str_is_printable(char *str){
	int i = 0;
	
	if(str == NULL)
		return str == NULL;

	while(str[i] != '\0'){
		if(str[i] >= '!'  && str[i] <= '~')
			return (0);
		i++;
	}
	return (1);
}
